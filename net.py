import sys
import re

class Net():
    def check_strings(self,strings):
        URL = 0
        IP = 0
        MAIL = 0
        line = []
        for letter in strings:
            if (letter == '\n'):
                line = ''.join(line)
                URL += self.check_url(line.strip())
                IP += self.check_ip(line.strip())
                MAIL += self.check_mail(line.strip())
                line = []
            else:
                line.append(letter)
        return URL,IP,MAIL

    def check_strace(self,line):
        URL = 0
        IP = 0
        MAIL = 0
        IP += self.check_ip(line.strip())
        URL += self.check_url(line.strip())
        MAIL += self.check_mail(line.strip())
        return URL,IP,MAIL

