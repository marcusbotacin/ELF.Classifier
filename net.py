    def check_ip(self,line):
        IP_regex = r'[0-9]+(?:\.[0-9]+){3}'

        ip_addr = re.findall(IP_regex, line)
        if ip_addr:
            return 1
        return 0


    def check_mail(self,line):
        mail_regex = r"([^@|\s]+@[^@]+\.[^@|\s]+)"

        mail = re.search(mail_regex, line, re.I)
        if mail:
            return 1
        return 0
